function divideToPackets(raw) {
    const tle_data = [];

    const lines = raw.split('\n');

    for (let i = 0; i < lines.length; i += 3) {
        tle_data.push({
            'id': i / 3 + 1,
            'name': lines[i],
            'line1': lines[i + 1],
            'line2': lines[i + 2]
        });
    }

    //give a packet to return
    return {
        retrieval_date: new Date().toISOString(),
        num_entries: tle_data.length,
        entries: tle_data
    }
}

//adds to local storage or retrieves from local storage
function load_data() {
    const raw = fetch_tle();
    const packet = divideToPackets(raw);

    //if local storage is empty, or if the data is different by an hour, update the local storage
    if (!localStorage.getItem('tle_data')) {
        localStorage.setItem('tle_data', JSON.stringify(packet));
    }
    
    const prev_stored_time = new Date(JSON.parse(localStorage.getItem('tle_data')).retrieval_date).getTime;
    const current_time = Date.now().getTime;
    if (current_time - prev_stored_time > 3600 * 1000) {
        localStorage.setItem('tle_data', JSON.stringify(packet));
    }

    return JSON.parse(localStorage.getItem('tle_data'));

}

